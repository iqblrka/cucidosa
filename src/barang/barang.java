    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) tabel_barang.getModel();
        model.setRowCount(0);
        try {
            Connection conn = koneksi.koneksiDB();
            String sql = "SELECT * FROM barang";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while (rs.next()) {
                String idBarang = rs.getString("Id_Barang");
                String namaBarang = rs.getString("Nama_Barang");
                int jumlahBarang = rs.getInt("Jumlah_Barang");
                double hargaBarang = rs.getDouble("Harga_Barang");
                double totalHarga = rs.getDouble("Total_Harga");
                String noKtp = rs.getString("No_Ktp");
                
                model.addRow(new Object[]{idBarang, namaBarang, jumlahBarang, hargaBarang, totalHarga, noKtp});
            }           
            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            System.err.println("Terjadi kesalahan: ");
            System.err.println(e.getMessage());
        }
    }
    public static void kurangiJumlahBarang(int id_barang, int jumlah_barang) {
        String query = "UPDATE barang SET Jumlah_Barang = Jumlah_Barang - ? WHERE Id_Barang = ?";
        try {
            Connection conn = koneksi.koneksiDB();
            PreparedStatement statement = conn.prepareStatement(query);
            statement.setInt(1, jumlah_barang);
            statement.setInt(2, id_barang);
            statement.executeUpdate();
            conn.close();
        } catch (SQLException e) {
            System.err.println("Terjadi kesalahan: ");
            System.err.println(e.getMessage());
        }  
}
     private boolean validateInputFields() {
    if (txtidbarang.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID Barang tidak boleh kosong", "Validasi Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    if (txtnamabarang.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama Barang tidak boleh kosong", "Validasi Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    try {
        int jumlah = Integer.parseInt(jumlahbarang.getText());
        if (jumlah <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah Barang harus lebih dari 0", "Validasi Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Jumlah Barang harus angka", "Validasi Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    try {
        double harga = Double.parseDouble(hargabarang.getText());
        if (harga <= 0) {
            JOptionPane.showMessageDialog(this, "Harga Barang harus lebih dari 0", "Validasi Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Harga Barang harus angka", "Validasi Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    if (noktp.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "No KTP tidak boleh kosong", "Validasi Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    return true;
}
   
