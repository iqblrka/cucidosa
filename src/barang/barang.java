    private void HAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HAPUSActionPerformed
 int row = tabel_barang.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Silahkan pilih data yang akan dihapus",
            "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    String idBarang = tabel_barang.getValueAt(row, 0).toString();
    int confirm = JOptionPane.showConfirmDialog(this,
        "Apakah anda yakin ingin menghapus barang dengan ID " + idBarang + "?",
        "Konfirmasi Hapus",
        JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try (Connection conn = koneksi.koneksiDB();
            PreparedStatement stmt = conn.prepareStatement(
                "DELETE FROM barang WHERE Id_Barang = ?")) {

            stmt.setString(1, idBarang); 

            int result = stmt.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this,
                    "Data berhasil dihapus!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);
                loadData(); 
            } else {
                JOptionPane.showMessageDialog(this,
                    "Gagal menghapus data",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Error menghapus data: " + e.getMessage(),
                "Database Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_HAPUSActionPerformed

