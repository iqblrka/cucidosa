    private void SEARCHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEARCHActionPerformed
        String searchKeyword = SEARCH.getText().trim();
        if (!searchKeyword.isEmpty()) {
            try (Connection conn = koneksi.koneksiDB()) {
                String sql = "SELECT * FROM barang WHERE Nama_Barang LIKE ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, "%" + searchKeyword + "%");

                ResultSet rs = stmt.executeQuery();
                DefaultTableModel model = (DefaultTableModel) tabel_barang.getModel();
                model.setRowCount(0);

                while (rs.next()) {
                    String idBarang = rs.getString("Id_Barang");
                    String namaBarang = rs.getString("Nama_Barang");
                    int jumlahBarang = rs.getInt("Jumlah_Barang");
                    double hargaBarang = rs.getDouble("Harga_Barang");
                    double totalHarga = rs.getDouble("Total_Harga");
                    String noKtp = rs.getString("No_Ktp");
                    model.addRow(new Object[]{idBarang, namaBarang, jumlahBarang, hargaBarang, totalHarga, noKtp});
                }

                if (model.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(this, "Data tidak ditemukan", "Pencarian Kosong", JOptionPane.INFORMATION_MESSAGE);
                }
                rs.close();
                stmt.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error during search: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Masukkan nama barang untuk pencarian!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_SEARCHActionPerformed

    private void btnEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEDITActionPerformed
       if (validateInputFields()) {
        try {
            Connection conn = koneksi.koneksiDB();
            String sql = "UPDATE barang SET Nama_Barang=?, Jumlah_Barang=?, Harga_Barang=?, Total_Harga=?, No_Ktp=? WHERE Id_Barang=?";
            
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, txtnamabarang.getText());
            stmt.setInt(2, Integer.parseInt(jumlahbarang.getText()));
            stmt.setDouble(3, Double.parseDouble(hargabarang.getText()));
            stmt.setDouble(4, Double.parseDouble(totalharga.getText()));
            stmt.setString(5, noktp.getText());
            stmt.setString(6, txtidbarang.getText());

            int result = stmt.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                loadData(); 
                clearInputFields(); 
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui data", "Error", JOptionPane.ERROR_MESSAGE);
            }
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid untuk Jumlah, Harga, dan Total Harga", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    }//GEN-LAST:event_btnEDITActionPerformed

