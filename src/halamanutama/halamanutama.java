private void calculateTotals() {
    double totalPembelian = 0;
    double totalPenjualan = 0;
    double totalGaji = 0;

    // Calculate total for pembelian
    DefaultTableModel modelBeli = (DefaultTableModel) tblpemasukantrkbeli.getModel();
    for (int i = 0; i < modelBeli.getRowCount(); i++) {
        Object totalHargaObj = modelBeli.getValueAt(i, 3); // total_harga column
        if (totalHargaObj instanceof Number) {
            totalPembelian += ((Number) totalHargaObj).doubleValue();
        }
    }

    // Calculate total for penjualan
    DefaultTableModel modelJual = (DefaultTableModel) tblpemasukantrkjual.getModel();
    for (int i = 0; i < modelJual.getRowCount(); i++) {
        Object hargaJasaObj = modelJual.getValueAt(i, 2); // harga_jasa column
        if (hargaJasaObj instanceof Number) {
            totalPenjualan += ((Number) hargaJasaObj).doubleValue();
        }
    }

    // Get total gaji from jgaji TextField
    try {
        String gajiText = jgaji.getText().replace(",", ""); // Remove thousands separator if any
        totalGaji = Double.parseDouble(gajiText);
    } catch (NumberFormatException e) {
        totalGaji = 0;
        System.err.println("Error parsing gaji: " + e.getMessage());
    }

    // Calculate total pengeluaran (pembelian + gaji)
    double totalPengeluaran = totalPembelian + totalGaji;

    // Calculate total laba (pemasukan - pengeluaran)
    double totalLaba = totalPenjualan - totalPengeluaran;

    // Format numbers for display
    NumberFormat formatter = NumberFormat.getInstance(Locale.US);
    formatter.setMaximumFractionDigits(2);
    formatter.setMinimumFractionDigits(2);

    // Set values to text fields
    jpembelian.setText(formatter.format(totalPembelian));
    jpemasukan.setText(formatter.format(totalPenjualan));
    jpengeluaran.setText(formatter.format(totalPembelian)); // Set jpengeluaran to show pembelian total
    textpengeluaran.setText(formatter.format(totalPengeluaran));
    hasillaba.setText(formatter.format(totalLaba));

}
  private void loadgaji() {
    try (Connection conn = DatabaseManager.getConnection();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery("SELECT SUM(Gaji) as total_gaji FROM karyawan")) {

        if (rs.next()) {
            double totalGaji = rs.getDouble("total_gaji");
            jgaji.setText(String.format(Locale.US, "%,.2f", totalGaji));
        } else {
            jgaji.setText("0.00");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, 
            "Error retrieving salary data: " + e.getMessage(), 
            "Database Error", 
            JOptionPane.ERROR_MESSAGE);
        jgaji.setText("Error");
        e.printStackTrace(); 
    }
}



