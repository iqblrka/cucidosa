private void loadTransaksiBeli() {
    DefaultTableModel model = (DefaultTableModel) tblpemasukantrkbeli.getModel();
    model.setRowCount(0); // Clear existing rows

    String sql = "SELECT * FROM transaksi_beli";
    try (Connection conn = DatabaseManager.getConnection();
         PreparedStatement stmt = conn.prepareStatement(sql);
         ResultSet rs = stmt.executeQuery()) {

        while (rs.next()) {
            Object[] row = {
                rs.getString("kode_pembelian"),
                rs.getDate("tanggal_pembelian"),  // You can format this date if needed
                rs.getDouble("Jumlah_pembelian"),
                rs.getDouble("total_harga"),
                rs.getString("No_Ktp_supplier"),
                rs.getString("Id_Barang")
            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}

